#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"
#include <iostream>

int main() {
    {
        Weapon club = Weapon("crude spiked club");
        HumanA bob("Bob", club);
        bob.attack();
        club.setType("some other type of club");
        bob.attack();
    }
    {
        Weapon club = Weapon("crude spiked club");
        HumanB jim("Jim");
        jim.setWeapon(club);
        jim.attack();
        club.setType("some other type of club");
        jim.attack();
    }
}

// For HumanA it's Reference to Weapon, HumanB it's pointer. Why?
// If it's a reference to an address, you need to declare it on initialization.
// Only in Human A it's sent as parameter with the name, on B it's referenced later, so Pointer on it.